# Texto dividido em duas linhas, dois prints
print("Além destas, outras cinco pessoas são alvos de prisão temporária – que valem por cinco dias, prorrogáveis por mais cinco. No total,")
print("dos 11 pedidos de prisão, seis foram expedidos a professores. A operação cumpre, ainda, 12 mandados de busca a apreensão.")

# Texto dividido com \n para quebra
print("Além destas, outras cinco pessoas são alvos de prisão temporária – que valem por cinco dias, prorrogáveis por mais cinco. No total,\n dos 11 pedidos de prisão, seis foram expedidos a professores. A operação cumpre, ainda, 12 mandados de busca a apreensão.")

# Texto com tres aspas segue o padrão digitado
print("""Além destas, outras cinco pessoas são alvos de prisão temporária – que valem por cinco dias, prorrogáveis por mais cinco. No total, 
dos 11 pedidos de prisão, seis foram expedidos a professores. A operação cumpre, ainda, 12 mandados de busca a apreensão.""")

# Texto concatenado
print("Além destas, outras cinco pessoas" +  "são alvos de prisão temporária – que valem por cinco dias, prorrogáveis por mais cinco.")

# Usando \ para não impactar o texto 
print("Além destas, outras cinco pessoas \"são alvos de prisão temporária\" – que valem por cinco dias, prorrogáveis por mais cinco.")

